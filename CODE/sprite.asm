/*
    INFO: DISPLAYS A 1 TILE SPRITE ON SCREEN
    INPUT: HL - ADDRESS OF TILE INDEX LIST, D - X POS, E - Y POS, A - SPRITE TABLE INDEX
    OUTPUT: NONE
    USES: AF, BC, DE, HL
*/
display1TileSprite:
;   SETUP HIGH BYTE OF VDP ADDRESS AND PORT
    LD BC, (hibyte(VRAMWRITE) | hibyte(SPRITE_TABLE)) * $100 + VDPCON_PORT
;   SET VDP ADDRESS FOR Y VALUES
    OUT (VDPCON_PORT), A
    OUT (C), B
    DEC C   ; DATA PORT
;   PREPARE Y VALUES
    DEC E   ; VDP DISPLAYS SPRITES AT (Y + 1), SO REMOVE 1 SO IT DISPLAYS CORRECTLY
;   SINGLE TILE SPRITE
    OUT (C), E
;   OTHER SPRITE TILE (HIDDEN)
    LD E, $C0
    OUT (C), E   ; SET Y TO 0
;   OTHER SPRITE TILE (HIDDEN)
    OUT (C), E   ; SET Y TO 0
;   OTHER SPRITE TILE (HIDDEN)
    OUT (C), E   ; SET Y TO 0
;   SET VDP ADDRESS FOR X AND INDEX VALUES
    ADD A, A
    ADD A, $80
    OUT (VDPCON_PORT), A
    INC C   ; CONTROL PORT
    OUT (C), B
    DEC C   ; DATA PORT
;   SPRITE TILE
    OUT (C), D    ; SET X
    OUTI
;   OTHER SPRITE TILE (HIDDEN)
    XOR A
    OUT (VDPDATA_PORT), A    ; SET X TO 0
    OUT (VDPDATA_PORT), A    ; SET INDEX TO 0
;   OTHER SPRITE TILE (HIDDEN)
    OUT (VDPDATA_PORT), A    ; SET X TO 0
    OUT (VDPDATA_PORT), A    ; SET INDEX TO 0
;   OTHER SPRITE TILE (HIDDEN)
    OUT (VDPDATA_PORT), A    ; SET X TO 0
    OUT (VDPDATA_PORT), A    ; SET INDEX TO 0
    RET



