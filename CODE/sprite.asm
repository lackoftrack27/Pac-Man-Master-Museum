/*
    INFO: DISPLAYS A 9 TILE SPRITE ON SCREEN
    INPUT: HL - ADDRESS OF TILE INDEX LIST, D - X POS, E - Y POS, A - SPRITE TABLE INDEX
    OUTPUT: NONE
    USES: AF, HL, DE, C, I

    ORDER: 1 4 7
           2 5 8
           3 6 9
*/
display9TileSprite:
    LD I, A
    LD C, VDPDATA_PORT
;   SET VDP ADDRESS FOR X AND INDEX VALUES
    ADD A, A
    ADD A, $80
    OUT (VDPCON_PORT), A
    LD A, hibyte(VRAMWRITE) | hibyte(SPRITE_TABLE)
    OUT (VDPCON_PORT), A
;   TOP-LEFT SPRITE TILE
    OUT (C), D    ; SET X
    OUTI
;   MIDDLE-LEFT SPRITE TILE
    OUT (C), D    ; SET X
    OUTI
;   BOTTOM-LEFT SPRITE TILE
    OUT (C), D    ; SET X
    OUTI
;   PREPARE EXTRA X VALUE
    LD A, $08
    ADD A, D
;   TOP-MIDDLE SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   MIDDLE-MIDDLE SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   BOTTOM-MIDDLE SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   PREPARE EXTRA X VALUE
    LD A, $10
    ADD A, D
;   TOP-RIGHT SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   MIDDLE-RIGHT SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   BOTTOM-RIGHT SPRITE TILE
    OUT (VDPDATA_PORT), A    ; SET X
    OUTI
;   SET VDP ADDRESS FOR Y VALUES
    LD A, I
    OUT (VDPCON_PORT), A
    LD A, hibyte(VRAMWRITE) | hibyte(SPRITE_TABLE)
    OUT (VDPCON_PORT), A
;   PREPARE Y VALUES
    DEC E   ; VDP DISPLAYS SPRITES AT (Y + 1), SO REMOVE 1 SO IT DISPLAYS CORRECTLY
    LD A, $08
    ADD A, E
    LD D, A ; D - MIDDLE Y
    LD A, $10
    ADD A, E ; A - BOTTOM Y
;   TOP-LEFT SPRITE TILE
    OUT (C), E
;   MIDDLE-LEFT SPRITE TILE
    OUT (C), D
;   BOTTOM-LEFT SPRITE TILE
    OUT (VDPDATA_PORT), A
;   TOP-MIDDLE SPRITE TILE
    OUT (C), E
;   MIDDLE-MIDDLE SPRITE TILE
    OUT (C), D
;   BOTTOM-MIDDLE SPRITE TILE
    OUT (VDPDATA_PORT), A
;   TOP-RIGHT SPRITE TILE
    OUT (C), E
;   MIDDLE-RIGHT SPRITE TILE
    OUT (C), D
;   BOTTOM-RIGHT SPRITE TILE
    OUT (VDPDATA_PORT), A
    RET